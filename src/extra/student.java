package extra;

public class student {

	
	private String name;
	public student(String name) {
		super();
		this.name = name;
	}
	/**
	 * @return the id
	 */
	
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	
}
